def get_city_population(city_name: str) -> int:
    """
    Get population for a Texas city
    Returns None if city not found
    """
    # Try exact match first
    if city_name in TEXAS_CITY_POPULATIONS_EXTENDED:
        return TEXAS_CITY_POPULATIONS_EXTENDED[city_name]
    
    # Try case-insensitive match
    city_lower = city_name.lower()
    for city, pop in TEXAS_CITY_POPULATIONS_EXTENDED.items():
        if city.lower() == city_lower:
            return pop
    
    return None